
DROP TABLE IF EXISTS Node;
CREATE TABLE Node (
	"Uuid" INTEGER PRIMARY KEY,
	"NcUuid" INTEGER DEFAULT 0 NOT NULL, 
	"NcTable" TEXT DEFAULT '' NOT NULL, 
	"NcType" TEXT DEFAULT '' NOT NULL, 
	"NcTitle" TEXT DEFAULT '' NOT NULL,
	"NcTimeCreated" INTEGER DEFAULT 0 NOT NULL, 
	"NcTimeLastChange" INTEGER DEFAULT 0 NOT NULL
	
);

